# Hashing
dictionary = {}

# Insertion
dictionary ["a"] = "apple"
dictionary ["b"] = "ball"
dictionary ["c"] = "cat"
dictionary ["d"] = "dog"

print(dictionary)

# Search
print(f"value is: {dictionary ["a"]}")
print(f"value is: {dictionary ["b"]}")
print(f"value is: {dictionary ["c"]}")
print(f"value is: {dictionary ["d"]}")

# Deletion
del dictionary ["a"]

print(dictionary)